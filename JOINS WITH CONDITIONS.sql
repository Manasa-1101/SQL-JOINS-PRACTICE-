/*16.Find the total number of students per course (use GROUP BY + JOIN).
17.	List courses with no students enrolled.*/

 SELECT COURSE_NAME,COUNT(S.STUDENT_ID) AS TOTAL_STUDENTS
 FROM COURSES AS C LEFT JOIN STUDENTS AS S
 ON C.COURSE_ID =S.COURSE_ID
 GROUP BY
 C.COURSE_NAME;

